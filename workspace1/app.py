import math

list1 = [1, 2, 3, 4, 5, 6]
list1_squared = []   # Lista para guardar los cuadrados
list1_square_roots = []  # Lista para guardar las raÃ­ces cuadradas

# Calcular cuadrados
for item in list1:
    list1_squared.append(item ** 2)

print('Squares of my list:')
for item in list1_squared:
    print(item)

# Calcular raÃ­ces cuadradas
for item in list1:
    list1_square_roots.append(math.sqrt(item))

print('Square roots of my list:')
for i in list1_square_roots:
    print(i)